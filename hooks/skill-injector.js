#!/usr/bin/env node
// UserPromptSubmit hook — Auto-detects technology keywords and suggests relevant skills.
//
// Scans user prompts for technology/framework mentions and injects skill references
// from REGISTRY.md so the agent knows which SKILL.md files to load.
// Inspired by oh-my-claudecode's skill-injector.
//
// Uses an embedded keyword→skill map (faster than reading REGISTRY.md each time).
// The map covers active skills only. Update when REGISTRY.md changes.

const fs = require('fs');
const os = require('os');
const path = require('path');

let input = '';
process.stdin.setEncoding('utf8');
process.stdin.on('data', chunk => input += chunk);
process.stdin.on('end', () => {
  try {
    const data = JSON.parse(input);
    const prompt = (data.prompt || data.message || '').toLowerCase().trim();

    if (!prompt || prompt.length < 8) {
      process.exit(0);
    }

    // Load dynamic keyword cache (generated by sync-skill-keywords.js at session start)
    let dynamicSkills = [];
    const cacheFile = path.join(os.homedir(), '.claude', 'cache', 'skill-keyword-map.json');
    try {
      if (fs.existsSync(cacheFile)) {
        const cache = JSON.parse(fs.readFileSync(cacheFile, 'utf8'));
        dynamicSkills = (cache.skills || []).map(s => [
          new RegExp(s.patterns.join('|'), 'i'),
          s.id, s.name, s.path
        ]);
      }
    } catch (e) {}

    // Technology keyword → skill mapping (from REGISTRY.md active skills)
    // Format: [regex, skill_id, skill_name, path]
    // Dynamic entries come first so newly created skills match before hardcoded ones
    const skillMap = [
      ...dynamicSkills,
      // Frameworks & languages
      [/\bnext\.?js\b|\bnextjs\b|\bapp\s?router\b|\brsc\b|\bserver\s?component/, 'FS-020', 'nextjs-developer', 'skills/fullstack-dev/nextjs-developer/'],
      [/\breact\s?native\b|\bexpo\b/, 'FS-026', 'react-native-expert', 'skills/fullstack-dev/react-native-expert/'],
      [/\breact\b(?!\s*native)/, 'FS-021', 'react-expert', 'skills/fullstack-dev/react-expert/'],
      [/\btypescript\b|\b\.tsx?\b/, 'FS-002', 'typescript-pro', 'skills/fullstack-dev/typescript-pro/'],
      [/\bpython\b|\b\.py\b|\bpip\b|\buv\b/, 'FS-001', 'python-pro', 'skills/fullstack-dev/python-pro/'],
      [/\bsql\b|\bquery\b|\bpostgres/, 'FS-012', 'sql-pro', 'skills/fullstack-dev/sql-pro/'],
      [/\bpostgresql?\b|\bpsql\b/, 'FS-052', 'postgres-pro', 'skills/fullstack-dev/postgres-pro/'],

      // Design & UI
      [/\bthree\.?js\b|\b3d\b|\bwebgl\b/, 'SK-007', 'threejs', 'skills/threejs/SKILL.md'],
      [/\bgsap\b|\banimation\b|\btween/, 'SK-042', 'gsap-core', 'skills/gsap-core/SKILL.md'],
      [/\bscrolltrigger\b|\bscroll\s?animation/, 'SK-044', 'gsap-scrolltrigger', 'skills/gsap-scrolltrigger/SKILL.md'],
      [/\bfrontend\s?design\b|\bcss\b|\btailwind\b|\bui\s?design/, 'SK-005', 'frontend-design', 'skills/frontend-design/SKILL.md'],
      [/\bui\/ux\b|\bdesign\s?system\b|\bcolor\s?palette/, 'SK-006', 'ui-design-stack', 'skills/ui-design-stack/SKILL.md'],
      [/\bfigma\b/, 'MCP-003', 'Figma MCP', 'Plugin: figma@claude-plugins-official'],
      [/\bshadcn\b/, 'MCP-002', 'shadcn MCP', '~/.claude/.mcp.json'],

      // Documents
      [/\bpdf\b/, 'SK-002', 'pdf', 'skills/pdf/SKILL.md'],
      [/\bword\b|\bdocx\b|\bdocument/, 'SK-001', 'docx', 'skills/docx/SKILL.md'],
      [/\bslide|pptx?\b|\bpresentation\b|\bdeck\b/, 'SK-003', 'pptx', 'skills/pptx/SKILL.md'],
      [/\bspreadsheet\b|\bxlsx?\b|\bexcel\b/, 'SK-004', 'xlsx', 'skills/xlsx/SKILL.md'],

      // Video & media
      [/\bremotion\b|\bvideo\s?(creation|generation|render)/, 'SK-008', 'remotion', 'skills/remotion/SKILL.md'],

      // Testing & automation
      [/\bplaywright\b|\bbrowser\s?automation\b|\be2e\s?test/, 'SK-009', 'playwright', 'skills/playwright/SKILL.md'],
      [/\btdd\b|\btest.driven/, 'CE-002', 'tdd', 'skills/context-engineering-kit/tdd/'],

      // Architecture
      [/\bapi\s?design\b|\bopenapi\b|\brest\s?api\b/, 'FS-028', 'api-designer', 'skills/fullstack-dev/api-designer/'],
      [/\bwebsocket\b|\breal.?time\b|\bws:\/\//, 'FS-050', 'websocket-engineer', 'skills/fullstack-dev/websocket-engineer/'],
      [/\brag\b|\bretrieval\s?augmented/, 'FS-057', 'rag-architect', 'skills/fullstack-dev/rag-architect/'],
      [/\bprompt\s?engineer/, 'FS-058', 'prompt-engineer', 'skills/fullstack-dev/prompt-engineer/'],

      // Security
      [/\bsecurity\s?(audit|review|scan)\b|\bvulnerabilit/, 'SC-001', 'sharp-edges', 'skills/trailofbits-security/sharp-edges/'],

      // MCP & skills
      [/\bmcp\s?server\b|\bmcp\s?tool\b/, 'SK-011', 'mcp-builder', 'skills/mcp-builder/SKILL.md'],
      [/\bvercel\b|\bdeploy\b/, 'SK-028', 'deploy-to-vercel', 'skills/deploy-to-vercel/SKILL.md'],

      // DevOps
      [/\bci\/cd\b|\bpipeline\b|\binfrastructure\b/, 'FS-031', 'devops-engineer', 'skills/fullstack-dev/devops-engineer/'],

      // LinkedIn
      [/\blinkedin\b/, 'SK-052', 'linkedin-poster', 'skills/linkedin-poster/SKILL.md'],

      // Writing quality
      [/\bwrite\b|\bdraft\b|\bedit\b|\bproofreading?\b|\bblog\s?post\b|\bessay\b|\bprose\b|\bcopy\b|\bcopywriting\b|\bcontent\b/, 'SK-053', 'stop-slop', 'skills/stop-slop/SKILL.md'],

      // Firebase
      [/\bfirebase\b|\bfirestore\b/, 'MCP-004', 'Firebase MCP', 'Plugin: firebase@claude-plugins-official'],
    ];

    const detected = [];
    const seen = new Set();

    for (const [pattern, id, name, skillPath] of skillMap) {
      if (pattern.test(prompt) && !seen.has(id)) {
        seen.add(id);
        detected.push({ id, name, path: skillPath });
        if (detected.length >= 2) break; // Max 2 skills per CLAUDE.md rules
      }
    }

    if (detected.length === 0) {
      process.exit(0);
    }

    const skillList = detected.map(s => `[${s.id}] ${s.name} → ${s.path}`).join('; ');
    const suggestion = `SKILL MATCH: ${skillList}. ` +
      `Load the SKILL.md if this task requires domain expertise. ` +
      `Max 2 specialist skills per context budget rules.`;

    const output = {
      hookSpecificOutput: {
        hookEventName: "UserPromptSubmit",
        additionalContext: suggestion
      }
    };
    process.stdout.write(JSON.stringify(output));

    // Log skill selection events for performance tracking (Improvement 2)
    const logDir = path.join(os.homedir(), '.claude', 'logs');
    try { fs.mkdirSync(logDir, { recursive: true }); } catch (e) {}
    const eventsPath = path.join(logDir, 'skill-events.jsonl');
    const sessionId = (data.session_id || '').slice(0, 16);
    for (const s of detected) {
      const event = JSON.stringify({
        ts: new Date().toISOString(),
        event: 'selected',
        skill_id: s.id,
        skill_name: s.name,
        session: sessionId,
      });
      try { fs.appendFileSync(eventsPath, event + '\n'); } catch (e) {}
    }

  } catch (e) {
    process.exit(0);
  }
});
